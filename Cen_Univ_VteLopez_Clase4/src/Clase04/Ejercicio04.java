public class Ejercicio04 {

	/**
	 * Muestra los números del 1 al 100 (ambos incluidos).
	 */
	public static void Mostrar1a100() {
		for(int i = 1;i<101;i++) {
			System.out.println("N°: " + i);
		}
	}
	
	/**
	 * @param args
	 */
	public static void main(String[] args) {
		Mostrar1a100();
	}
}
